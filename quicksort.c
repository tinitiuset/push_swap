#include "push_swap.h"

void	quicksort(t_number **a, t_number **b)
{
	s("a", a);
	p("b", b, a);
	p("b", b, a);
	p("b", b, a);
	r("a", a);
	rr("a", a);
	rrall(a, b);
}
